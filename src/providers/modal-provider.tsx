"use client";

import { ProjectDrawer } from "@/components/project-drawer";
import { useEffect, useState } from "react";

export const ModalProvider = () => {
  const [isMounted, setIsMounted] = useState(false);

  useEffect(() => {
    setIsMounted(true);
  }, []);

  if (!isMounted) return null;
  return (
    <>
      <ProjectDrawer />
    </>
  );
};
